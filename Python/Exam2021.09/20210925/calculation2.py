# 1. 비교연산
print(2>3)  #false
print(15<30) # true
print(1.5 >=0)
print(3<=3)
print("팙팗팙" == "팙팗팗")
print("111111" != "111111")

# 2. 논리연산
print( 4<6 and 10 >= 10) # true and true -> true
print("포기하지 말아요" != "포기하지 말아요" or "나는 할 수 있다." == "나는 할 수 있다.")
print(not 5==5) # not True -> false

# 3. 멤버십 연산
print("a" in "abc") # 포함되어 있다면 True
print("d" not in "abc") #포함되어 있지않다면 True
